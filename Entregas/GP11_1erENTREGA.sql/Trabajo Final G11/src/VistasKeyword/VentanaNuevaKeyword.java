    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(validarEntradas()){
            if(txtNameKey.getText().isEmpty()){
            txtErrorName.setText("ingrese una palabra para la Keyword");
            txtNameKey.requestFocus();
            
            }else{
            String keyword = txtNameKey.getText();
               Keywords keywords = new Keywords(keyword);
                keywordData.crearKeyword(keywords);
                txtNameKey.setEditable(false);
                btnSave.setEnabled(false);
                    btnNw.setEnabled(true);
                    txtErrorName.setForeground(Color.green);
                      txtErrorName.setText("*Nueva Keyword agregada con Ã©xito.");
                      txtNameKey.setText("");
         }
       }
    }//GEN-LAST:event_btnSaveActionPerformed

